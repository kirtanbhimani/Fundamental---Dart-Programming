// 23. Difference between inheritance and encapsulation. B5. Difference 
// between inheritance and abstraction.

/*

Inheritance is the process through which the properties 
(state and behaviour) of parent is reflected in the child. 
In OOP, inheritance is beneficial because it allows for 
code reusability and polymorphism.

Encapsulation is a mechanism of wrapping the properties 
together as a single unit. In OOP, think of a class as 
an encapsulated unit of states(variables) and behaviours
(methods/functions). Encapsulation allows Data-Hiding and 
Data Abstraction. Through abstraction, the user/programmer 
using the encapsulated class doesn't need to know the 
values of variables or the implementation of the methods. 
They just need to know which method to call to do which task.

 */