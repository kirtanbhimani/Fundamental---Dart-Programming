// 32. How do you restrict a member of a class from inheriting by its sub 
// classes? 

/*
In Dart, you can restrict a member of a class from being inherited by its 
subclasses by marking it as final. When a member of a class is marked as final, 
it cannot be overridden or hidden by subclasses. This effectively prevents 
subclasses from inheriting or modifying the behavior of that member.
*/