// 36. When to overload a method in Dart and when to override it? 

/*

In Dart, method overloading and method overriding are different concepts used in different scenarios.

1. Method Overloading:
Method overloading refers to defining multiple methods with the same name in 
the same class, but with different parameter lists. This allows you to have 
multiple versions of the method that perform similar tasks but with different 
sets of parameters.

Use method overloading when you want to provide different ways to call a method 
with varying parameters or types, or when you want to provide default values 
for optional parameters.

2. Method Overriding:
Method overriding refers to providing a new implementation for a method in a 
subclass that is already present in its superclass. This allows subclasses to
provide their own specialized behavior while maintaining a common interface 
with the superclass.

Use method overriding when you want to provide specific behavior for a method 
in a subclass that differs from the behavior defined in the superclass.


*/