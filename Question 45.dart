// 45. Difference between object oriented and object based language. \

/*
1. Object-Oriented Programming (OOP):

Object-oriented programming is a programming paradigm based on the concept of 
"objects", which can contain data (attributes or properties) and code (methods 
or functions).
OOP emphasizes the organization of software as a collection of objects that 
interact with each other through methods.
Key principles of OOP include encapsulation, inheritance, and polymorphism.
Examples of object-oriented programming languages include Java, C++, Python, 
and Ruby.

2. Object-Based Programming:

Object-based programming is a subset of object-oriented programming that lacks 
some of the key features of full-fledged OOP languages.
In object-based programming, data and behavior are still grouped into objects, 
but the language may not support all OOP concepts such as inheritance or 
polymorphism.
Object-based languages typically support features like encapsulation and object 
creation, but they may lack support for inheritance or method overriding.
Examples of object-based programming languages include JavaScript (before the 
introduction of classes in ECMAScript 6) and VBScript.
*/